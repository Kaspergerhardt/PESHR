def get_dataset_window(patient: str, phase: str) -> tuple[int, int]:
    """
    Haal het handmatig gedefinieerde tijdvenster op voor een patiënt en fase.

    Parameters
    ----------
    patient : str
        Patiëntcode als string met 3 cijfers, bv. "014"
    phase : str
        Eén van: preswitch, t0, t30, t60, t90, t120, t150, t180, t210

    Returns
    -------
    tuple[int, int]
        (start, end) in seconden
    """
    if phase not in DATASETS_BY_PHASE:
        raise ValueError(
            f"Onbekende phase: {phase}. "
            f"Beschikbaar: {list(DATASETS_BY_PHASE.keys())}"
        )

    match = next(
        (item for item in DATASETS_BY_PHASE[phase] if item[0] == patient),
        None,
    )

    if match is None:
        raise ValueError(
            f"Geen dataset gevonden voor patiënt {patient} in fase {phase}"
        )

    raw_t_start, raw_t_end = match[1]

    if raw_t_start is Ellipsis or raw_t_end is Ellipsis:
        raise ValueError(
            f"Tijdvenster voor patiënt {patient} in fase {phase} is nog niet ingevuld."
        )

    return raw_t_start, raw_t_end